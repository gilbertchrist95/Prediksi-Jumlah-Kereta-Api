    private void initData()
    {   String[] test = new String[5];
        try {
            File file = new File ("Inisialisasi.txt");
            FileReader fileReader = new FileReader(file);
            BufferedReader br =  new BufferedReader(fileReader);
            
            ArrayList<String> list = new ArrayList<>();
            
            while (true){
                String line = br.readLine();
                if (line == null) {
                    break;
                }
                list.add(line);
            }
            
            for (int i = 0; i < 5; i++) {
                test[i] = list.get(i);
            }
            
            textFieldKecepatanBelajar.setText(test[2]);
            textFieldMinimumError.setText(test[3]);
            textFieldHiddenLayer.setText(test[4]);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormAplikasi.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FormAplikasi.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }
    
    
       
    
    
