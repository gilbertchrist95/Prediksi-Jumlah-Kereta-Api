    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        double learningRate,minError;
        int hiddenLayer;    
        
        if (textFieldKecepatanBelajar.getText().equals("")|| 
                textFieldMinimumError.getText().equals("")||
                textFieldHiddenLayer.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Data tidak lengkap!","Error",
                    JOptionPane.ERROR_MESSAGE);  
        }
        else{
            int layerMasukan = 6;
            int layerKeluaran=1;
            learningRate = Double.parseDouble(textFieldKecepatanBelajar.getText());
            minError  = Double.parseDouble(textFieldMinimumError.getText());
            hiddenLayer = Integer.parseInt(textFieldHiddenLayer.getText());
        
            KInisialisasi = new CInisialisasi(layerMasukan,layerKeluaran,learningRate,minError,hiddenLayer);
            KInisialisasi.simpanInisialisasi(layerMasukan,layerKeluaran,learningRate, minError, hiddenLayer);
            JOptionPane.showMessageDialog(null,"Data disimpan!");
            
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

