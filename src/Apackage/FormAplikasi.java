    private void buttonBukaLocationMasukanPembelajaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBukaLocationMasukanPembelajaranActionPerformed
        // TODO add your handling code here:
        textAreaMasukanPembelajaran.setText("");
        if (!(choose.showOpenDialog(this)== JFileChooser.APPROVE_OPTION)) {
            return;
        }
        csvFileMasukan = choose.getSelectedFile();
        if (csvFileMasukan == null)
        {
            return;
        }
        fileNameMasukanPembelajaran = csvFileMasukan.getAbsolutePath();
        textFieldLocationMasukanPembelajaran.setText(fileNameMasukanPembelajaran);
        //parseCSV();
        
        Kinput.setDataInput1(fileNameMasukanPembelajaran);
        rows = Kinput.getJumlahBarisPembelajaran();
        cols = Kinput.getJumlahKolomPembelajaran();
        Object[][] dataPembelajaran = new Double[rows][cols];
       
         for (int i = 0; i < 6; i++){
             textAreaMasukanPembelajaran.append(columnNamesInput[i]+"\t\t");
         }
        textAreaMasukanPembelajaran.append("\n");
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < cols; j++) {
                dataPembelajaran[i][j]= Kinput.getDataInputPembelajaran(i, j);
                double dp1 = (double) dataPembelajaran[i][j];
                int dtPembelajaran = (int) dp1;
                textAreaMasukanPembelajaran.append(String.valueOf(dtPembelajaran)+"\t\t");  
//                textAreaMasukanPembelajaran.append(String.valueOf(dataPembelajaran[i][j])+"\t");  
            }
            textAreaMasukanPembelajaran.append("\n");
            
        }
        
        
    }//GEN-LAST:event_buttonBukaLocationMasukanPembelajaranActionPerformed

